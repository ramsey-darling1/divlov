    <h4>Post on the DivLov Wall</h4>
    <p>A post on the divlov wall can be seen by everyone on the site.</p>
    <p>When a user responds to your post, that response goes directly to your inbox.</p>
    <p>When you respond to that response, it goes directly to that user.</p>
    <p>Respond to other user's posts too! Remember your response is only seen by that user.</p>
    <br />
